// Ejercicio 119: Estudiar la diferencia entre las funciones Math.floor y Math.ceil.

// Math.floor(): Redondea al entero menor más cercano
// Math.ceil(): Redondea al tenero mayor más cercano

let valor = 3.6;

console.log(Math.floor(valor));
console.log(Math.ceil(valor));

console.log();

valor = -3.6;

console.log(Math.floor(valor));
console.log(Math.ceil(valor));
