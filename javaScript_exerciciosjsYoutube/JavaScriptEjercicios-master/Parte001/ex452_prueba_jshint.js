function sumar(a, b, c){
    return a + b + c;
}

// curly
let contador = 0;
while(contador <= 10){
    ++contador;
}

// eqeqeq
if (contador === 10){
    console.log('La variable contador es igual a 10.');
} else {
    console.log('La variable contador NO es igual a 10.');
}
