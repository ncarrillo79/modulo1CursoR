// Ejercicio 646: Usar la función Math.trunc() para truncar un número de punto flotante.

console.log(Math.trunc(13.37));
// expected output: 13

console.log(Math.trunc(42.84));
// expected output: 42

console.log(Math.trunc(0.123));
// expected output: 0

console.log(Math.trunc(-0.123));
// expected output: -0

console.log(Math.trunc(Math.PI));
// expected output: 3

console.log(Math.trunc(Math.E));
// expected output: 2
