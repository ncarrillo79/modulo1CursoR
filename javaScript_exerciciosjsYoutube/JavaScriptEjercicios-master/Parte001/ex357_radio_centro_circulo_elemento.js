// Ejercicio 357: Calcular el radio y centro de un círculo para incrustar en un elemento.

let ancho = 300;
let alto = 200;

// Cálculo del radio del círculo:
let radioCirculo = Math.min(ancho, alto) / 2;

// Cálculo del centro del círculo:
let x = ancho / 2;
let y = alto / 2;

console.log(radioCirculo);
