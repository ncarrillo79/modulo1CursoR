// Ejercicio 149: Eliminar un elemento de un arreglo con el método shift.

// Solución:

let numeros = [1, 2, 3, 4, 5];

console.log(numeros.length);
console.log(numeros);

console.log();

let numeroEliminado = numeros.shift();

console.log(numeros.length);
console.log('Número eliminado:', numeroEliminado);
console.log(numeros);
